#!/usr/local/bin/perl
# get all images into one html
print "<HTML>\n";
print "<TITLE>Examples</TITLE>";
print "<BODY>\n";
print "<!-- DO NOT EDIT THIS FILE, EDIT THE PERL FILE CREATING IT -->\n";
print "<h3>Examples, blurred using a 5x5 Gaussian filter with sigma=1.5 and decimated by 2:1 in each direction</h3>";

my $dir = "./result_examples/";
opendir(DIR, $dir) or die "wrong dir";
my @files = grep {/.*\.png/} readdir DIR;
close DIR;
foreach my $file (@files) {
	if (not $file =~ m/freq/) {
		my $file1 = $file;
		$file1 =~ s/\.png//;
		print "<h2> Image example: </h2>  <img src=$dir$file>\n";
		print "<h3> Frequency response: </h3>  <img src=$dir${file1}_freq.png>\n";
	}
}

print "</BODY>\n";
print "</HTML>\n";
